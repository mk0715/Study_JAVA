package day04;

public class ifelse01 {
	public static void main(String[] args) {
		// if~else구문은 참일때 실행할 구문과 거짓일때 실행할 구문을 작성합니다.
		// if~else구문은 한 구문으로 간주되기때문에 연산횟수를 절약합니다.
		// else문은 if문 없이 단독으로 쓰일 수 없습니다.
		// else문은 조건식을 작성할 필요가 없습니다.
		int a = 15;
		
		if(a == 15) {
			System.out.println("15입니다.");
			
		} else {
			System.out.println("15가 아닙니다.");
		}
	}

}
