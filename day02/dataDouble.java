package day02;

public class dataDouble {

	public static void main(String[] args) {
		// 실수 표현을 할 때는 float, double 자료형을 씁니다.
		// 기본적으로 쓰여진 실수 숫자는 double로 간주됩니다.
		// 만약 float형으로 실수 숫자를 표현하고 싶다면
		// 숫자 가장 오른쪽에 F라고 적어줍니다.
		float a = 1.1f; // 좌변의 a변수에 double 1.1을 넣으려 해서 에러발생. f를 넣어주어야 float형으로 사용댐.
		double b = 1.1;
		System.out.println(a);
		System.out.println(b);

	}

}
